type ColorConfig = { variant?: number, opacity?: number };
type ColorStyle = {
    variant?: number,
    opacity?: number
};

const defaultColorArgs = {
    variant: 500,
    opacity: 1
}


export class Colors {
    static black(config?: ColorStyle): RGBA {
        return Colors.getValue('black', config);
    }

    static white(config?: ColorStyle): RGBA {
        return Colors.getValue('white', config);
    }

    static blue(config?: ColorStyle): RGBA {
        return Colors.getValue('blue', config);
    }

    static red(config?: ColorStyle): RGBA {
        return Colors.getValue('red', config);
    }

    private static getValue(colorName: string, config?: ColorStyle): RGBA {
        const { variant, opacity } = { ...defaultColorArgs, ...config };

        if (cs[colorName] === undefined) {
            throw new Error('Unknown color ' + colorName);
        }

        const colors = cs[colorName];
        let idx;

        if (colorName === 'black' || colorName === 'white') {
            idx = 0;
        } else if (variant === 50) {
            idx = 0;
        } else if (variant === 950) {
            idx = colors.length - 1;
        } else {
            idx = variant / 100;
        }

        const rgbColor = hexToRgb(colors[idx]);
        rgbColor[3] = opacity;

        return rgbColor;
    }
}



