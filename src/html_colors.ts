const lowerKeys = new Set(Object.keys(htmlColors).map(c => c.toLowerCase()));

export function isHtmlColor(colorName: string) {
    return lowerKeys.has(colorName);
}


export function getHtmlColor(colorName: string): RGBA {
    for (const [name, rgba] of Object.entries(htmlColors)) {
        if (name.toLowerCase() === colorName.toLowerCase()) {
            return rgba as RGBA;
        }
    }

    throw new Error(`Color name "${colorName}" not an HTML color code`);
}

